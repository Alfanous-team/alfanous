# Arabizi digraph mappings: two Latin characters → one Arabic letter
# These create ambiguity with their component single-character mappings,
# which is why arabizi_to_arabic_list() returns multiple candidates.
ARABIZI_DIGRAPHS = {
    "sh": "\u0634",  # ش sheen
    "th": "\u062B",  # ث tha
    "kh": "\u062E",  # خ kha
    "dh": "\u0630",  # ذ dhal
    "gh": "\u063A",  # غ ghain
    "3'": "\u063A",  # غ ghain (number-apostrophe notation: 3'ayr=غير)
    "ch": "\u0634",  # ش sheen (dialectal: Chokr=شكر)
    "ah": "\u0629",  # ة ta marbuta (terminal -ah suffix: Salah=صلاة, Rahmah=رحمة)
    "eh": "\u0629",  # ة ta marbuta (Rule D: terminal feminine suffix)
}

# Vowels excluded from gemination in Arabizi conversion (Rule C).
_ARABIZI_VOWELS = frozenset("aeiou")

# Two-character word-initial prefixes that map to the definite article ال.
# Covers standard (al/el) and sun-letter assimilation forms.
_ARABIZI_AL_PREFIXES_2 = frozenset([
    "al", "el",
    "ar", "er",
    "an", "en",
    "as", "es",
    "ad", "ed",
])

# Three-character prefixes for ش assimilation (ash/esh: Ash-shaytan → الشيطان).
_ARABIZI_AL_PREFIXES_3 = frozenset(["ash", "esh"])

# Hard cap on the number of candidate Arabic strings produced by the recursive
# _convert() helper inside arabizi_to_arabic_list().  Without a cap, adversarial
# or very long inputs can cause combinatorial explosion: a 10-char string with
# 5 Arabic mappings per position yields up to 5^10 = 10 million candidates,
# exhausting hundreds of MB of memory.  The Quran word-set filter that follows
# is only meaningful on a small list of plausible forms, so stopping at 1,000
# candidates loses no recall for legitimate queries.
_MAX_ARABIZI_CANDIDATES = 1000


def arabizi_to_arabic_list(string, ignore=""):
    """Convert an Arabizi string to a list of potential Arabic strings.

    Arabizi (Arabic chat alphabet) uses Latin characters and digits to
    approximate Arabic sounds.  Because certain digraphs (e.g. "sh") can
    be read either as a single Arabic letter (ش) or as two separate letters
    (س + ه), the mapping is ambiguous.  This function explores all
    possible segmentations and returns every distinct Arabic string that
    can result from the input.

    Additional rules applied during conversion:

    - Rule B: Word-initial ``el`` / ``al`` maps to the definite article ال.
      Assimilation variants ``ar``, ``er``, ``an``, ``en``, ``as``, ``es``,
      ``ad``, ``ed`` and the three-character prefix ``ash`` / ``esh`` are
      also handled (e.g. ``ar-ra7man`` → includes الرحمن).
    - Rule C: Doubled consonants (e.g. ``ll``, ``tt``) produce the letter
      followed by a shadda (ّ), representing gemination.
    - Rule D: Initial ``a`` or ``2`` also yields ``أ`` (hamza-on-alef);
      initial ``i`` or ``e`` also yields ``إ`` (hamza-under-alef, e.g.
      ``Iman``→إيمان, ``Iblis``→إبليس); terminal ``a`` also yields ``ى``;
      the digraphs ``eh`` and ``ah`` at any position yield ``ة``
      (ta marbuta, e.g. ``Salah``→صلاة, ``Rahmah``→رحمة).
    - Short vowel omission: every vowel (``a``, ``e``, ``i``, ``o``, ``u``)
      also yields an empty string, because short vowels are not written in
      unvocalized Arabic script.  This allows e.g. ``salameh`` to produce
      ``سلامة`` (alongside ``سالامة``).
    - The hyphen ``-`` is treated as a transparent separator so that forms
      like ``al-7amd`` and ``ar-ra7man`` are handled correctly (the hyphen
      is consumed without adding any Arabic character).  When ``-`` is
      included in the *ignore* set, the ``if char in ignore`` guard takes
      precedence over the ARABIZI2UNICODE lookup, so the hyphen is passed
      through as a literal ``-`` in the output (useful for preserving
      Whoosh search operators).

    :param string: Arabizi input string (case-insensitive)
    :param ignore: characters that are passed through unchanged
    :return: list of potential Arabic strings (deduplicated)
    """

    def _convert(s, at_word_start=True):
        if not s:
            return {""}

        results = set()
        c = s[0].lower()

        # Word boundary: a space resets the word-start flag for what follows
        if s[0] == " ":
            for suffix in _convert(s[1:], at_word_start=True):
                results.add(" " + suffix)
                if len(results) >= _MAX_ARABIZI_CANDIDATES:
                    return results
            return results

        # Rule B: definite article prefix → ال
        # Check 3-char prefixes first (e.g. ash/esh: Ash-shaytan → الشيطان)
        if (at_word_start and len(s) >= 3
                and s[0] not in ignore and s[1] not in ignore and s[2] not in ignore):
            prefix3 = s[:3].lower()
            if prefix3 in _ARABIZI_AL_PREFIXES_3:
                for suffix in _convert(s[3:], at_word_start=False):
                    results.add("\u0627\u0644" + suffix)  # ال
                    if len(results) >= _MAX_ARABIZI_CANDIDATES:
                        return results

        # Then check 2-char prefixes (al/el and sun-letter assimilation variants)
        if (at_word_start and len(s) >= 2
                and s[0] not in ignore and s[1] not in ignore):
            prefix2 = s[:2].lower()
            if prefix2 in _ARABIZI_AL_PREFIXES_2:
                for suffix in _convert(s[2:], at_word_start=False):
                    results.add("\u0627\u0644" + suffix)  # ال
                    if len(results) >= _MAX_ARABIZI_CANDIDATES:
                        return results

        # Early-exit check after Rule B (prefix candidates alone could saturate the cap)
        if len(results) >= _MAX_ARABIZI_CANDIDATES:
            return results

        # Rule C: Gemination — doubled consonant → letter + shadda (ّ U+0651).
        # Also produces the letter WITHOUT shadda so that the unvocalized Quran
        # wordset (which has no diacritic marks) can be matched (e.g. "Jannah"
        # → جنة as well as جنّة).
        # Note: digraphs like "sh" are treated as units; doubling them (e.g. "shsh")
        # produces two distinct letters (شش), not a geminated one (شّ), because
        # the digraph check below takes precedence over each individual character.
        if (len(s) >= 2 and s[0] not in ignore and s[1] not in ignore
                and c == s[1].lower() and c in ARABIZI2UNICODE
                and c not in _ARABIZI_VOWELS):
            for ac in ARABIZI2UNICODE[c]:
                for suffix in _convert(s[2:], at_word_start=False):
                    results.add(ac + "\u0651" + suffix)  # with shadda
                    results.add(ac + suffix)              # without shadda (unvocalized match)
                    if len(results) >= _MAX_ARABIZI_CANDIDATES:
                        return results

        # Try digraph first (Rule A: multi-char codes before single chars)
        if len(s) >= 2 and s[0] not in ignore and s[1] not in ignore:
            digraph = s[:2].lower()
            if digraph in ARABIZI_DIGRAPHS:
                for suffix in _convert(s[2:], at_word_start=False):
                    results.add(ARABIZI_DIGRAPHS[digraph] + suffix)
                    if len(results) >= _MAX_ARABIZI_CANDIDATES:
                        return results

        # Try single-character mapping
        if s[0] in ignore:
            for suffix in _convert(s[1:], at_word_start=False):
                results.add(s[0] + suffix)
                if len(results) >= _MAX_ARABIZI_CANDIDATES:
                    return results
        elif c in ARABIZI2UNICODE:
            arabic_chars = ARABIZI2UNICODE[c]  # list of possible Arabic chars
            suffixes = _convert(s[1:], at_word_start=False)
            # Short vowel omission: in unvocalized Arabic, short vowels are not
            # written.  Generate an empty-string candidate for every vowel so that
            # e.g. "salameh" → 'سلامة' (in addition to 'سالامة').
            if c in _ARABIZI_VOWELS:
                results.update(suffixes)
                if len(results) >= _MAX_ARABIZI_CANDIDATES:
                    return results
            if c == "a":
                # Rule D: initial 'a' → أ (U+0623) in addition to ا
                if at_word_start:
                    for suffix in suffixes:
                        results.add("\u0623" + suffix)  # أ + rest
                        if len(results) >= _MAX_ARABIZI_CANDIDATES:
                            return results
                # Rule D: terminal 'a' → ى (U+0649) and ة (U+0629) in addition to ا
                # ة covers feminine endings: Ganna=جنة, Ra7ma=رحمة
                if len(s) == 1:
                    for suffix in suffixes:  # suffixes == {""} when terminal
                        results.add("\u0649" + suffix)  # ى
                        results.add("\u0629" + suffix)  # ة (feminine ta marbuta)
                        if len(results) >= _MAX_ARABIZI_CANDIDATES:
                            return results
                # Standard ا mapping always included (arabic_chars == ["ا"])
                for ac in arabic_chars:
                    for suffix in suffixes:
                        results.add(ac + suffix)
                        if len(results) >= _MAX_ARABIZI_CANDIDATES:
                            return results
            elif c == "2":
                # Rule D: initial '2' also yields أ (hamza-on-alef) and إ (hamza-
                # under-alef) in addition to ء.  Covers: 2anta=أنت, 2iman=إيمان.
                if at_word_start:
                    for suffix in suffixes:
                        results.add("\u0623" + suffix)  # أ + rest
                        results.add("\u0625" + suffix)  # إ + rest
                        if len(results) >= _MAX_ARABIZI_CANDIDATES:
                            return results
                for ac in arabic_chars:
                    for suffix in suffixes:
                        results.add(ac + suffix)  # ء + rest
                        if len(results) >= _MAX_ARABIZI_CANDIDATES:
                            return results
            elif c in ("i", "e") and at_word_start:
                # Rule D extension: initial 'i'/'e' also yields إ (hamza-under-alef,
                # U+0625) in addition to ي.  Covers: Iman→إيمان, Iblis→إبليس,
                # Ikhlas→إخلاص, Enta→إنت.
                # Note: arabic_chars is [ي (ya, U+064A)] for both 'i' and 'e'.
                for suffix in suffixes:
                    results.add("\u0625" + suffix)  # إ + rest
                    if len(results) >= _MAX_ARABIZI_CANDIDATES:
                        return results
                for ac in arabic_chars:
                    for suffix in suffixes:
                        results.add(ac + suffix)  # ي + rest (standard ya mapping)
                        if len(results) >= _MAX_ARABIZI_CANDIDATES:
                            return results
            elif c in ("u", "o") and at_word_start:
                # Rule D extension: initial 'u'/'o' also yields أ (hamza-on-alef,
                # U+0623) in addition to و.  Covers: Ummah→أمة, Ommah→أمة
                # (dialectal form of أمة where users write initial 'o' for the
                # glottal-stop vowel sound), and similar Islamic/dialectal forms
                # where a word-initial short vowel is represented with 'u' or 'o'.
                for suffix in suffixes:
                    results.add("\u0623" + suffix)  # أ + rest
                    if len(results) >= _MAX_ARABIZI_CANDIDATES:
                        return results
                for ac in arabic_chars:
                    for suffix in suffixes:
                        results.add(ac + suffix)  # و + rest (standard waw mapping)
                        if len(results) >= _MAX_ARABIZI_CANDIDATES:
                            return results
            else:
                for ac in arabic_chars:
                    for suffix in suffixes:
                        results.add(ac + suffix)
                        if len(results) >= _MAX_ARABIZI_CANDIDATES:
                            return results
                # Rule D extension: terminal 'h' or 't' at word-end also yields
                # ة (ta marbuta).  In standard transliteration the feminine suffix
                # ة is written as trailing 'h' (Salah=صلاة, Rahmah=رحمة) or 't'
                # (Zakat=زكاة, Hayat=حياة, Salat=صلاة).  The 'at'/'ah' digraphs
                # already handle many cases, but a bare terminal 'h'/'t' (when
                # the preceding vowel was omitted) also needs this candidate.
                if c in ("h", "t") and (len(s) == 1 or (len(s) > 1 and s[1] == " ")):
                    for suffix in suffixes:
                        results.add("\u0629" + suffix)  # ة
                        if len(results) >= _MAX_ARABIZI_CANDIDATES:
                            return results
        else:
            # Keep unmapped characters as-is
            for suffix in _convert(s[1:], at_word_start=False):
                results.add(s[0] + suffix)
                if len(results) >= _MAX_ARABIZI_CANDIDATES:
                    return results

        # Return the accumulated set directly — no per-level list(set(...))
        # conversion needed because results is already a set with duplicates
        # eliminated incrementally via set.add().
        return results

    raw = _convert(string, at_word_start=True)
    # Normalize ءا → آ (alef madda, U+0622): in Arabic orthography the sequence
    # hamza + alef is written as آ, e.g. قرءان → قرآن (Quran), آية (aya).
    # raw is already a set; iterate over a snapshot so we can add to it safely.
    for c in list(raw):
        replaced = c.replace("\u0621\u0627", "\u0622")  # ءا → آ
        if replaced != c:
            raw.add(replaced)
    return list(raw)


def filter_candidates_by_wordset(candidates, wordset):
    """Filter Arabizi transliteration candidates to those present in *wordset*.

    Each candidate may be a multi-word string (space-separated tokens, as
    produced when the input contains spaces).  A candidate is accepted when
    **every** space-separated token appears in *wordset*.  If no candidates
    pass the filter the original *candidates* list is returned unchanged so
    that non-Quranic Arabizi input still produces suggestions.

    :param candidates: list of Arabic candidate strings (output of
        :func:`arabizi_to_arabic_list`)
    :param wordset: a set or frozenset of known Arabic words to filter against
    :return: filtered list, or *candidates* unchanged if no entry matches
    """
    filtered = [c for c in candidates
                if all(tok in wordset for tok in c.split())]
    return filtered if filtered else list(candidates)


def transliterate(mode, string, ignore="", reverse=False):
    """ encode & decode different  romanization systems """

    if reverse:
        mapping = ROMANIZATION_SYSTEMS_MAPPINGS_REVERSED.get(mode) or {}
    else:
        mapping = ROMANIZATION_SYSTEMS_MAPPINGS.get(mode) or {}

    result = []
    for char in string:
        if char not in ignore and mapping.get(char):
            result.append(mapping[char])
        else:
            result.append(char)
    return "".join(result)
